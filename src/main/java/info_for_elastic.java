public class info_for_elastic {

    public String url;
    public String topic = "";
    public String date = "";
    public String author = "";
    public String filling = "";
    public int what_to_do = 0;


    public info_for_elastic(String url, String topic, String date, String author, String filling, int what_to_do){
        this.url = url;
        this.topic = topic;
        this.date = date;
        this.author = author;
        this.filling = filling;
        this.what_to_do = what_to_do;
    }

}
